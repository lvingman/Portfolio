function hide(){
    var toolbarContainer = document.getElementById("toolbarContainer");
    var cuerpoContainer = document.getElementById("cuerpoContainer");
    opacity = Number(window.getComputedStyle(cuerpoContainer).getPropertyValue("opacity"));
    if(opacity<1){
        opacity = opacity-0.1;
        toolbarContainer.style.opacity = opacity;
        cuerpoContainer.style.opacity = opacity;
    }
    else{
        clearInterval(intervalID);
    }
}

